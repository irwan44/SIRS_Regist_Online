package averin.sirs.com.Model;

public class isiSpinner {
    private String id, ket;

    public isiSpinner() {
    }

    public isiSpinner(String id, String ket) {
        this.id = id;
        this.ket = ket;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getKet() {
        return ket;
    }

    public void setKet(String ket) {
        this.ket = ket;
    }

}
